public class Posicao
{
	private int x,y;


        /****** Construtor ******/


	public Posicao(int x, int y)
	{
		this.x = x;
		this.y = y;
	}


        /****** Getters ******/


	public int getX()
	{
		return this.x;
	}


	public int getY()
	{
		return this.y;
	}


        /****** Setters ******/


	public void setX(int v)
	{
		this.x = v;
	}


	public void setY(int v)
	{
		this.y = v;
	}
}
